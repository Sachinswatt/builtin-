l = [10,20,30,40]

l.append(50)     #[10,20,30,40,50]
l.extend([70,80])#[10,20,30,40,50,70,80]
l.insert(0,100)  #[100,10,20,30,40,50,70,80]
l.remove(20)     #[100,10,30,40,50,70,80]
l.pop()          #[100,10,30,40,50,70]
l.pop(0)         #[10,30,40,50,70]
l.clear()        #[]

lt = [10,30,40,50,70]

print(lt.index(40))#2
print(lt.count(90))#0
lt.insert(0,100)    #[100,10,30,40,50,70]
lt.sort()           #[10,30,40,50,70,100]
lt.reverse()        #[100,70,50,40,30,10]
new = lt.copy()     #[new]
print(lt)
print(new)